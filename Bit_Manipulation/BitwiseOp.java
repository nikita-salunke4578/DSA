package Bit_Manipulation;

public class BitwiseOp {
    public static void main(String[] args) {
        //Bitwise AND
        System.out.println("Bitwise AND of 5 & 6:");
        System.out.println(5 & 6);

        //Bitwise OR
        System.out.println("Bitwise OR of 3 |1:");
        System.out.println(3 | 1);

        //Bitwise XOR
        System.out.println("Bitwise XOR of 5 ^ 6:");
        System.out.println(5 ^ 6);

        //Bitwise Left Shift
        System.out.println(" Bitwise Left shift of 7<<2:");
        System.out.println(7 << 2);

        //Bitwise Right Shift
        System.out.println("Bitwise Right Shift of 4>>3:");
        System.out.println(4 >> 3);

        //Binary one's complement or Binary Not
        System.out.println("Binary Not of 15:");
        System.out.println(~15);
    }
}
